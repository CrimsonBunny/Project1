MINVALUE 1;

CREATE TABLE ACCOUNT_TYPE
(
  ACCOUNT_TYPE_ID   BIGINT    NOT NULL,
  MNEMONIC  VARCHAR(100)  NOT NULL UNIQUE,
  ACCOUNT_TYPE_NAME VARCHAR(255)    NOT NULL,
  CREATION_DATE DATE    NOT NULL,
  PRIMARY KEY (ACCOUNT_TYPE_ID)
);

CREATE TABLE Account_Transaction
(
  Transaction_ID   BIGINT    NOT NULL,
  Account_TypeID   BIGINT    NOT NULL,
  BalanceID   BIGINT    NOT NULL,
  AMOUNT   BIGINT    NOT NULL,
  Transaction_Date DATE    NOT NULL,
  PRIMARY KEY (Transaction_ID),
  CONSTRAINT FK_ACCOUNT_TYPE_ID FOREIGN KEY (Account_TypeID) REFERENCES ACCOUNT_TYPE (ACCOUNT_TYPE_ID)
);

CREATE TABLE Account_Balance (
    BalanceID BIGINT PRIMARY KEY,
    MemberID BIGINT,
    Account_TypeID BIGINT,
    Balance BIGINT,
    CONSTRAINT fk_Acc_Bal_Type FOREIGN KEY (Account_TypeID) REFERENCES Account_Type (ACCOUNT_TYPE_ID)
);